    private void prepareForm() {
        pnlName.init("*Name:", "", "");
        pnlName.setValidator(new RequiredTextValidator());
        pnlDate.init("*Opening date:", "", "");
        pnlDate.setValidator(new RequiredDateValidator());
        pnlDays.init("*Duration in days:", "", "");
        pnlDays.setValidator(new RequiredDurationInDaysValidator());

        comboExhibit.removeAllItems();
        try {
            List<Exhibit> exhibits = exhibitService.getAll();
            for (Exhibit exhibit : exhibits) {
                comboExhibit.addItem(exhibit);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

        comboMuseum.removeAllItems();
        try {
            List<Museum> museums = museumService.getAll();
            for (Museum museum : museums) {
                comboMuseum.addItem(museum);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

        TableModel tm = new ExhibitionTableModel(new ArrayList<>());
        tblExhibition.setModel(tm);
    }

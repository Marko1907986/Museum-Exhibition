    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Exhibit exhibit = (Exhibit) comboExhibit.getSelectedItem();
        Museum museum = (Museum) comboMuseum.getSelectedItem();
        try {
            String name = pnlName.getValue();
            String jdate = pnlDate.getValue();
            Date date = new SimpleDateFormat("dd.MM.yyyy.").parse(jdate);
            int durationInDays = Integer.parseInt(pnlDays.getValue());
            Exhibition exhibition = new Exhibition(museum, exhibit, date, name, durationInDays);
            int option = JOptionPane.showConfirmDialog(null, "Are you sure?", "Add object?", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                ExhibitionTableModel etm = (ExhibitionTableModel) tblExhibition.getModel();
                etm.addRow(exhibition);
                comboMuseum.setEnabled(false);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Please update values!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        ExhibitionTableModel etm = (ExhibitionTableModel) tblExhibition.getModel();
        List<Exhibition> exhibitions = etm.getData();
        try {
            int option = JOptionPane.showConfirmDialog(null, "Are you sure?", "Save list?", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                exhibitionService.save(exhibitions);
                JOptionPane.showMessageDialog(this, "List is saved!");
                dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error:" + ex.getMessage());
            dispose();
        }
    }//GEN-LAST:event_btnSaveActionPerformed


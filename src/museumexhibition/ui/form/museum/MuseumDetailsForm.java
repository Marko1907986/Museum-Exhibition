    private void prepareForm() throws Exception {
        List<City> cities = cityService.getAll();
        comboCity.setModel(new DefaultComboBoxModel<>(cities.toArray()));

        pnlName.init("*Name:", String.valueOf(museum.getName()), "");
        pnlName.setValidator(new RequiredTextValidator());
        pnlAddress.init("*Address:", String.valueOf(museum.getAddress()), "");
        pnlAddress.setValidator(new RequiredTextValidator());
        comboCity.setSelectedItem(museum.getCity());
    }

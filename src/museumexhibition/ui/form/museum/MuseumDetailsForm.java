    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Long id = museum.getId();
        try {
            String name = pnlName.getValue();
            String address = pnlAddress.getValue();
            City city = (City) comboCity.getSelectedItem();
            Museum museum = new Museum(id, name, address, city);
            int option = JOptionPane.showConfirmDialog(null, "Are you sure?", "Update museum?", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                museumService.update(museum);
                JOptionPane.showMessageDialog(this, "Museum is successfully updated!");
                dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error:" + ex.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int result = JOptionPane.showConfirmDialog(null, "Are you sure?", "Delete museum?", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            try {
                museumService.delete(museum.getId());
                JOptionPane.showMessageDialog(this, "Museum is successfully deleted!");
                dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error:" + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

